//check whether it is perfect square or not
    
var num =parseInt(prompt('Enter the number to check perfect square or not'));
var squareroot = Math.sqrt(num);
var i= Math.floor(squareroot);
if(i*i== num)
{
 alert("Number is a perfect square");
}
else
{
alert("Number is not a perfect square");
}
                
            
  
 
           